class RemappedSubset(Dataset):
    """Filters dataset to allowed_classes and remaps labels to 0…k-1."""

    def __init__(self, base_dataset, allowed_classes: List[int],
                 indices: Optional[List[int]] = None):
        self.base = base_dataset
        self.class_to_new = {c: i for i, c in enumerate(sorted(allowed_classes))}
        if indices is not None:
            self.indices = indices
        else:
            allowed_set = set(allowed_classes)
            targets = getattr(base_dataset, "targets", None)
            if targets is not None:
                self.indices = [i for i, lbl in enumerate(targets)
                                if int(lbl) in allowed_set]
            else:
                self.indices = [i for i, (_, lbl) in enumerate(base_dataset)
                                if int(lbl) in allowed_set]

    def __len__(self) -> int:
        return len(self.indices)

    def __getitem__(self, idx: int):
        img, lbl = self.base[self.indices[idx]]
        return img, self.class_to_new[int(lbl)]


# ─────────────────────────────────────────────────────────────────────────────
# Task Generator
# ─────────────────────────────────────────────────────────────────────────────

class TaskGenerator:
    TASK_CLASSES = [list(range(i, i + 10)) for i in range(0, 100, 10)]

    config = SimpleNamespace(
        input_dim       = 3072,
        num_classes     = 10,
        num_tasks       = 10,
        criterion       = nn.CrossEntropyLoss(),
        task_classes    = TASK_CLASSES,
        grads_per_task  = 150,
        max_directions  = 750,
    )

    # Hypernetwork target — ResNet-32 with BatchNorm (canonical)
    target_network = ResNet32CIFAR(num_classes=10)

    # Standalone projection target — ResNet-32 depth with GroupNorm
    solo_target = MultiHeadTargetCNN

    _train_data: Optional[datasets.CIFAR100] = None
    _test_data:  Optional[datasets.CIFAR100] = None

    @classmethod
    def _load(cls) -> None:
        if cls._train_data is not None:
            return
        print(" [TaskGenerator] Initializing base CIFAR-100 datasets...")
        tf_train = transforms.Compose([
            transforms.RandomHorizontalFlip(),
            transforms.RandomCrop(32, padding=4),
            transforms.ToTensor(),
            transforms.Normalize((0.5071, 0.4867, 0.4408),
                                  (0.2675, 0.2565, 0.2761)),
        ])
        tf_test = transforms.Compose([
            transforms.ToTensor(),
            transforms.Normalize((0.5071, 0.4867, 0.4408),
                                  (0.2675, 0.2565, 0.2761)),
        ])
        cls._train_data = datasets.CIFAR100(
            root="./data", train=True,  download=True, transform=tf_train)
        cls._test_data  = datasets.CIFAR100(
            root="./data", train=False, download=True, transform=tf_test)

    @classmethod
    def _make_split(cls, dataset, allowed_classes: List[int],
                    batch_size: int = 32, shuffle: bool = True,
                    desc: str = "Processing Task") -> DataLoader:
        allowed_set = set(allowed_classes)
        indices = [i for i, lbl in
                   enumerate(tqdm(dataset.targets,
                                  desc=f"  {desc}", leave=False))
                   if lbl in allowed_set]
        return DataLoader(
            RemappedSubset(dataset, allowed_classes, indices),
            batch_size=batch_size, shuffle=shuffle,
            num_workers=2, pin_memory=True,
        )

    @classmethod
    def generate(cls, task_id: int,
                 batch_size: int = 32) -> Tuple[DataLoader, DataLoader]:
        assert 0 <= task_id < cls.config.num_tasks, \
            f"task_id must be in [0, {cls.config.num_tasks}), got {task_id}"
        cls._load()
        allowed = cls.TASK_CLASSES[task_id]
        prefix  = f"Task {task_id + 1} (classes {allowed[0]}-{allowed[-1]})"
        return (
            cls._make_split(cls._train_data, allowed,
                            batch_size=batch_size, shuffle=True,
                            desc=f"{prefix} Train"),
            cls._make_split(cls._test_data,  allowed,
                            batch_size=batch_size, shuffle=False,
                            desc=f"{prefix} Test"),
        )